    private void jTextArea1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextArea1KeyPressed
        // TODO add your handling code here:
        label.setText("KeyPressed sobre el text area");
        JOptionPane.showMessageDialog(null, 
                "Keycode:"+evt.getExtendedKeyCode());
        JOptionPane.showMessageDialog(null, 
                "keychar:"+evt.getKeyChar());
        JOptionPane.showMessageDialog(null, 
                "KeyLocation:"+evt.getKeyLocation());
        if(evt.getKeyLocation() == 4){
            label.setText(label.getText()+", pad numerico");
        }
        if(evt.getKeyLocation() == 1){
            label.setText(label.getText()+", "
                    + "teclado alfa-numerico");
        }
        if(evt.getKeyLocation() == 2){
            label.setText(label.getText()+", izquierdo");
        }
        if(evt.getKeyLocation() == 3){
            label.setText(label.getText()+", derecho");
        }
        JOptionPane.showMessageDialog(null, 
                "Es tecla de evento:"+evt.isActionKey());
    }//GEN-LAST:event_jTextArea1KeyPressed

